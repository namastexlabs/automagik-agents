# Convenient commands for managing Automagik services

# Automagik Agents Management
agent() {
    case "$1" in
        "start")
            # Check current status
            local service_available=false
            local container_available=false
            local service_running=false
            local container_running=false
            
            # Check if service is available and running
            if systemctl list-unit-files | grep -q automagik-agents 2>/dev/null; then
                service_available=true
                if systemctl is-active --quiet automagik-agents 2>/dev/null; then
                    service_running=true
                fi
            fi
            
            # Check if container is available and running
            if docker ps -a | grep -q automagik_agents 2>/dev/null; then
                container_available=true
                if docker ps | grep -q automagik_agents 2>/dev/null; then
                    container_running=true
                fi
            fi
            
            # If both are running, show status
            if [ "$service_running" = true ] && [ "$container_running" = true ]; then
                echo "‚ö†Ô∏è  Both service AND container are running!"
                echo "   Service: ‚úÖ Running"
                echo "   Container: ‚úÖ Running"
                echo "üí° Use 'agent status' for detailed information"
                return 0
            fi
            
            # If one is running, show which one
            if [ "$service_running" = true ]; then
                echo "‚úÖ automagik-agents service is already running"
                return 0
            fi
            
            if [ "$container_running" = true ]; then
                echo "‚úÖ automagik-agents container is already running"
                return 0
            fi
            
            # Nothing is running, decide what to start
            if [ "$service_available" = true ] && [ "$container_available" = true ]; then
                # Both are available, ask user
                echo "üîß Both systemd service and Docker container are available."
                echo "Which would you like to start?"
                echo "1) Systemd service"
                echo "2) Docker container"
                read -p "Choose option (1-2): " start_choice
                
                case $start_choice in
                    1)
                        echo "üîß Starting automagik-agents service..."
                        sudo systemctl start automagik-agents
                        sleep 2
                        if systemctl is-active --quiet automagik-agents; then
                            echo "‚úÖ automagik-agents service started successfully"
                            echo "üì° API available at: http://localhost:18881"
                        else
                            echo "‚ùå Failed to start automagik-agents service"
                            echo "üìã Check logs with: agent logs"
                        fi
                        ;;
                    2)
                        echo "üîß Starting automagik-agents container..."
                        docker start automagik_agents
                        sleep 2
                        if docker ps | grep -q automagik_agents; then
                            echo "‚úÖ automagik-agents container started successfully"
                            echo "üì° API available at: http://localhost:18881"
                        else
                            echo "‚ùå Failed to start automagik-agents container"
                            echo "üìã Check logs with: agent logs"
                        fi
                        ;;
                    *)
                        echo "‚ùå Invalid choice. Operation cancelled."
                        ;;
                esac
                
            elif [ "$service_available" = true ]; then
                # Only service available
                echo "üîß Starting automagik-agents service..."
                sudo systemctl start automagik-agents
                sleep 2
                if systemctl is-active --quiet automagik-agents; then
                    echo "‚úÖ automagik-agents service started successfully"
                    echo "üì° API available at: http://localhost:18881"
                else
                    echo "‚ùå Failed to start automagik-agents service"
                    echo "üìã Check logs with: agent logs"
                fi
                
            elif [ "$container_available" = true ]; then
                # Only container available
                echo "üîß Starting automagik-agents container..."
                docker start automagik_agents
                sleep 2
                if docker ps | grep -q automagik_agents; then
                    echo "‚úÖ automagik-agents container started successfully"
                    echo "üì° API available at: http://localhost:18881"
                else
                    echo "‚ùå Failed to start automagik-agents container"
                    echo "üìã Check logs with: agent logs"
                fi
                
            else
                echo "‚ùå No automagik-agents service or container found"
                echo "üí° Run the installer first"
            fi
            ;;
        "stop")
            local service_running=false
            local container_running=false
            local stopped_something=false
            
            # Check what's running
            if systemctl is-active --quiet automagik-agents 2>/dev/null; then
                service_running=true
            fi
            
            if docker ps | grep -q automagik_agents 2>/dev/null; then
                container_running=true
            fi
            
            # If both are running, ask what to stop
            if [ "$service_running" = true ] && [ "$container_running" = true ]; then
                echo "‚ö†Ô∏è  Both service AND container are running!"
                echo "Which would you like to stop?"
                echo "1) Systemd service only"
                echo "2) Docker container only"
                echo "3) Both service and container"
                read -p "Choose option (1-3): " stop_choice
                
                case $stop_choice in
                    1)
                        echo "üõë Stopping automagik-agents service..."
                        sudo systemctl stop automagik-agents
                        echo "‚úÖ automagik-agents service stopped"
                        stopped_something=true
                        ;;
                    2)
                        echo "üõë Stopping automagik-agents container..."
                        docker stop automagik_agents
                        echo "‚úÖ automagik-agents container stopped"
                        stopped_something=true
                        ;;
                    3)
                        echo "üõë Stopping both service and container..."
                        sudo systemctl stop automagik-agents
                        docker stop automagik_agents
                        echo "‚úÖ Both automagik-agents service and container stopped"
                        stopped_something=true
                        ;;
                    *)
                        echo "‚ùå Invalid choice. Operation cancelled."
                        ;;
                esac
                
            else
                # Stop whatever is running
                if [ "$service_running" = true ]; then
                    echo "üõë Stopping automagik-agents service..."
                    sudo systemctl stop automagik-agents
                    echo "‚úÖ automagik-agents service stopped"
                    stopped_something=true
                fi
                
                if [ "$container_running" = true ]; then
                    echo "üõë Stopping automagik-agents container..."
                    docker stop automagik_agents
                    echo "‚úÖ automagik-agents container stopped"
                    stopped_something=true
                fi
            fi
            
            if [ "$stopped_something" = false ]; then
                echo "‚ÑπÔ∏è  automagik-agents is not running"
            fi
            ;;
        "restart")
            # Check what's currently running
            local service_running=false
            local container_running=false
            
            if systemctl is-active --quiet automagik-agents 2>/dev/null; then
                service_running=true
            fi
            
            if docker ps | grep -q automagik_agents 2>/dev/null; then
                container_running=true
            fi
            
            if [ "$service_running" = false ] && [ "$container_running" = false ]; then
                echo "‚ÑπÔ∏è  Nothing is currently running. Use 'agent start' to start."
                return 0
            fi
            
            # If both are running, ask what to restart
            if [ "$service_running" = true ] && [ "$container_running" = true ]; then
                echo "‚ö†Ô∏è  Both service AND container are running!"
                echo "Which would you like to restart?"
                echo "1) Systemd service only"
                echo "2) Docker container only"
                echo "3) Both service and container"
                read -p "Choose option (1-3): " restart_choice
                
                case $restart_choice in
                    1)
                        echo "üîÑ Restarting automagik-agents service..."
                        sudo systemctl restart automagik-agents
                        sleep 2
                        if systemctl is-active --quiet automagik-agents; then
                            echo "‚úÖ automagik-agents service restarted successfully"
                        else
                            echo "‚ùå Failed to restart automagik-agents service"
                        fi
                        ;;
                    2)
                        echo "üîÑ Restarting automagik-agents container..."
                        docker restart automagik_agents
                        sleep 2
                        if docker ps | grep -q automagik_agents; then
                            echo "‚úÖ automagik-agents container restarted successfully"
                        else
                            echo "‚ùå Failed to restart automagik-agents container"
                        fi
                        ;;
                    3)
                        echo "üîÑ Restarting both service and container..."
                        sudo systemctl restart automagik-agents
                        docker restart automagik_agents
                        sleep 2
                        echo "‚úÖ Both automagik-agents service and container restarted"
                        ;;
                    *)
                        echo "‚ùå Invalid choice. Operation cancelled."
                        ;;
                esac
                
            else
                # Restart whatever is running
                if [ "$service_running" = true ]; then
                    echo "üîÑ Restarting automagik-agents service..."
                    sudo systemctl restart automagik-agents
                    sleep 2
                    if systemctl is-active --quiet automagik-agents; then
                        echo "‚úÖ automagik-agents service restarted successfully"
                    else
                        echo "‚ùå Failed to restart automagik-agents service"
                    fi
                fi
                
                if [ "$container_running" = true ]; then
                    echo "üîÑ Restarting automagik-agents container..."
                    docker restart automagik_agents
                    sleep 2
                    if docker ps | grep -q automagik_agents; then
                        echo "‚úÖ automagik-agents container restarted successfully"
                    else
                        echo "‚ùå Failed to restart automagik-agents container"
                    fi
                fi
            fi
            ;;
        "status")
            echo "üìä Automagik Agents Status:"
            echo
            
            # Check systemd service
            if command -v systemctl &> /dev/null && systemctl list-unit-files | grep -q automagik-agents; then
                echo "üîß Service Status:"
                sudo systemctl status automagik-agents --no-pager --lines=3
                echo
            fi
            
            # Check Docker container
            if command -v docker &> /dev/null && docker ps -a | grep -q automagik_agents; then
                echo "üê≥ Container Status:"
                docker ps -a | grep automagik_agents
                echo
                
                # Check health if running
                if docker ps | grep -q automagik_agents; then
                    local health=$(docker inspect automagik_agents --format='{{.State.Health.Status}}' 2>/dev/null || echo "unknown")
                    echo "üîó Container Health: $health"
                fi
            fi
            
            # Quick API health check
            echo "üîó API Health Check:"
            if curl -s http://localhost:18881/health > /dev/null 2>&1; then
                echo "‚úÖ API is responding at http://localhost:18881"
            else
                echo "‚ùå API is not responding"
            fi
            ;;
        "logs")
            if systemctl is-active --quiet automagik-agents 2>/dev/null; then
                # Service logs
                if command -v ccze &> /dev/null; then
                    echo "üìã Showing automagik-agents service logs (press Ctrl+C to exit)..."
                    sudo journalctl -u automagik-agents.service -f | ccze -A
                else
                    echo "üìã Showing automagik-agents service logs (press Ctrl+C to exit)..."
                    sudo journalctl -u automagik-agents.service -f
                fi
            elif docker ps | grep -q automagik_agents 2>/dev/null; then
                # Container logs
                echo "üìã Showing automagik-agents container logs (press Ctrl+C to exit)..."
                docker logs automagik_agents -f
            else
                echo "‚ùå No running automagik-agents service or container found"
                echo "üí° Try: agent start"
            fi
            ;;
        "dev")
            echo "üîß Starting automagik-agents in development mode..."
            echo "üìÇ Make sure you're in the project directory and virtual environment is activated"
            echo "üí° Tip: Use 'source .venv/bin/activate' to activate the virtual environment"
            echo
            read -p "Continue? (y/N): " -n 1 -r
            echo
            if [[ $REPLY =~ ^[Yy]$ ]]; then
                python -m src --reload
            else
                echo "‚ùå Development mode cancelled"
            fi
            ;;
        "health")
            echo "üîç Checking automagik-agents health..."
            if curl -s http://localhost:18881/health > /dev/null 2>&1; then
                echo "‚úÖ API is healthy and responding"
                echo "üì° Available endpoints:"
                echo "  ‚Ä¢ API: http://localhost:18881"
                echo "  ‚Ä¢ Docs: http://localhost:18881/docs"
                echo "  ‚Ä¢ Health: http://localhost:18881/health"
            else
                echo "‚ùå API is not responding"
                
                # Check what's running
                if systemctl is-active --quiet automagik-agents 2>/dev/null; then
                    echo "‚ÑπÔ∏è  Service is running but API might be starting up"
                    echo "üí° Try: agent logs"
                elif docker ps | grep -q automagik_agents 2>/dev/null; then
                    echo "‚ÑπÔ∏è  Container is running but API might be starting up"
                    echo "üí° Try: agent logs"
                else
                    echo "‚ÑπÔ∏è  Neither service nor container is running"
                    echo "üí° Try: agent start"
                fi
            fi
            ;;
        "update"|"quick-update")
            echo "‚ö° Running quick update to deploy code changes..."
            echo "üîß This will detect what's changed and rebuild only what's needed"
            echo
            
            # Find the installer script
            local installer_script=""
            
            # Try to find the installer script
            for search_path in \
                "$(pwd)/scripts/install/setup.sh" \
                "$HOME/workspace/am-agents-labs/scripts/install/setup.sh" \
                "/home/*/workspace/am-agents-labs/scripts/install/setup.sh" \
                "$(find /home -name setup.sh -path "*/am-agents-labs/scripts/install/*" 2>/dev/null | head -1)"; do
                
                if [ -f "$search_path" ]; then
                    installer_script="$search_path"
                    break
                fi
            done
            
            if [ -n "$installer_script" ]; then
                echo "üìç Found installer at: $installer_script"
                echo "üöÄ Starting quick update..."
                "$installer_script" --component agents --mode quick-update
            else
                echo "‚ùå Could not find the installer script"
                echo "üí° Please run from the project directory:"
                echo "    ./scripts/install/setup.sh --component agents --mode quick-update"
            fi
            ;;
        "rebuild")
            echo "üîÑ Smart Rebuild: Rebuilding with optimizations based on changes..."
            echo "üîß This uses the 'smart' quick update mode"
            echo
            
            # Find the installer script
            local installer_script=""
            
            # Try to find the installer script
            for search_path in \
                "$(pwd)/scripts/install/setup.sh" \
                "$HOME/workspace/am-agents-labs/scripts/install/setup.sh" \
                "/home/*/workspace/am-agents-labs/scripts/install/setup.sh" \
                "$(find /home -name setup.sh -path "*/am-agents-labs/scripts/install/*" 2>/dev/null | head -1)"; do
                
                if [ -f "$search_path" ]; then
                    installer_script="$search_path"
                    break
                fi
            done
            
            if [ -n "$installer_script" ]; then
                echo "üìç Found installer at: $installer_script"
                echo "üöÄ Starting smart rebuild..."
                # Pass --non-interactive to avoid update mode prompt
                "$installer_script" --component agents --mode quick-update --non-interactive
            else
                echo "‚ùå Could not find the installer script"
                echo "üí° Please run from the project directory:"
                echo "    ./scripts/install/setup.sh --component agents --mode quick-update"
            fi
            ;;
        "help"|"--help"|"-h"|"")
            echo "ü§ñ Automagik Agents Management Commands:"
            echo
            echo "  agent start     - Start the service or container"
            echo "  agent stop      - Stop the service or container"
            echo "  agent restart   - Restart the service or container"
            echo "  agent status    - Show service/container status"
            echo "  agent logs      - Show live logs (with colors if ccze available)"
            echo "  agent health    - Check API health and endpoints"
            echo "  agent update    - Quick update to deploy code changes (interactive)"
            echo "  agent rebuild   - Smart rebuild with optimizations (non-interactive)"
            echo "  agent dev       - Start in development mode (manual)"
            echo "  agent help      - Show this help message"
            echo
            echo "üì° Service URLs:"
            echo "  ‚Ä¢ API: http://localhost:18881"
            echo "  ‚Ä¢ Documentation: http://localhost:18881/docs"
            echo "  ‚Ä¢ Health Check: http://localhost:18881/health"
            echo
            echo "üí° Works with both systemd services and Docker containers"
            ;;
        *)
            echo "‚ùå Unknown command: $1"
            echo "üí° Use 'agent help' to see available commands"
            ;;
    esac
}

